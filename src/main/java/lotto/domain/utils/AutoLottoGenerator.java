package lotto.domain.utils;

import lotto.domain.Lotto;
import lotto.domain.Number;
import lotto.domain.NumberSet;

import java.util.ArrayList;
import java.util.Collections;
import java.util.List;

public class AutoLottoGenerator{

    private static final int LOTTO_SIZE = 6;

    public static Lotto makeLotto() {
        List<Number> autoGeneratedLottoNumbers = new ArrayList<>();

        for (int key : NumberSet.lottoNumbers.keySet()) {
            autoGeneratedLottoNumbers.add(NumberSet.of(key));
        }

        Collections.shuffle(autoGeneratedLottoNumbers);
        return new Lotto(autoGeneratedLottoNumbers.subList(0, LOTTO_SIZE));
    }
}
